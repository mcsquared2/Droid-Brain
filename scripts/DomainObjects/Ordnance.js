class Ordnance {
    constructor(name, baseDamage, additionalDamage, encumbrance, restricted, price, rarity, blastRadius, abilityNotes, bookSet, source){
        this.Name = name;
        this.BaseDamage = baseDamage;
        this.AdditionalDamage = additionalDamage;
        this.Encumbrance = encumbrance;
        this.Restricted = restricted;
        this.Price = price;
        this.Rarity = rarity;
        this.BlastRadius = blastRadius;
        this.AbilityNotes = abilityNotes;
        this.BookSet = bookSet;
        this.Source = source;
    }
}